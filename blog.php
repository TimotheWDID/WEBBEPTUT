						Aspect ergonomique
					</h3>

					<p style="color: white; margin-top: 0px; line-height: 25px;">Ensuite, l’ergonomie. Cette notion implique que le site serve de <b>guide</b> en quelque sorte à l’utilisateur, et que ce dernier ne soit pas là à cliquer partout à la recherche d’une aide pour se sortir de ce véritable labyrinthe à ses yeux. C’est donc pour cette raison qu’un site ergonomique est si important, ainsi l’internaute aura une impression <b>agréable</b> de <b>fluidité</b> et reviendra à coup sûr !</p>

