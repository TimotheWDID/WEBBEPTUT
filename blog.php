						Comment créer un site web ?
					</h2>

					<p style="color: white; margin-top: 0px; line-height: 25px;">Il va de soi qu’un webdesigner se pose souvent cette question quand il décide d’entreprendre une <b>application web</b>, un nouveau projet. Cela dit, celui-ci doit faire preuve de rigueur et de précision pour jongler entre les deux points essentiels pour un webdesign efficace : l’aspect <b>graphique</b> et l’aspect <b>ergonomique</b>. </p>

					<h3 style="border-left: 3px solid white ; font-size: 30px; color: white; margin-top: 10px; padding-left: 20px;">
						Aspect graphique
					</h3>

					<p style="color: white; margin-top: 0px; line-height: 25px;">Le premier point paraît évident pour tout le monde, il faut bien sûr que le site soit accueillant et qu’il n’agresse pas le visiteur, au risque de perdre une importante clientèle, mais là où cela a l’air plus subtil, c’est que le graphisme doit être <b>cohérent</b> avec le site en question, pour cibler un certain public, il faut en utiliser les <b>codes</b>. Par exemple, imaginez un site à l’apparence épurée et presque luxueuse, et les produits sont des boîtes de conserve lowcost vendus en lots ! Le visiteur serait totalement déboussolé !</p>

